-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
cRP = {}
Tunnel.bindInterface("vrp_racewater",cRP)
-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
local race = math.random(5)
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAYMENTS
-----------------------------------------------------------------------------------------------------------------------------------------
local payments = {
	[1] = { 1000,1200 },
	[2] = { 1000,1200 },
	[3] = { 1000,1200 },
	[4] = { 1000,1200 },
	[5] = { 1000,1200 }
}
-----------------------------------------------------------------------------------------------------------------------------------------
-- RACESELECT
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.raceSelect()
	return parseInt(race)
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAYMENTMETHOD
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.paymentMethod()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local value = math.random(payments[race][1],payments[race][2])

		vRP.giveInventoryItem(user_id,"dollars2",parseInt(value),true)
		TriggerClientEvent("vrp_sound:source",source,"coin",0.5)
	end
end